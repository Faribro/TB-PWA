export const t = (
  key: string,
  lang: SonicLanguage,
  vars?: Record<string, string | number>
): string => {
  const phrases = SONIC_PHRASES[key];
  if (!phrases) return key;
  let text = phrases[lang] ?? phrases['en'];
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
};
